from typing import Dict, List, Optional, Any


def _create_career(
    id: str,
    name: str,
    category: str,
    subcategory: str,
    description: str,
    required_skills: List[str],
    preferred_skills: List[str],
    education_min: str,
    education_pref: str,
    education_alt: List[str],
    salary_inr_entry: tuple,
    salary_inr_mid: tuple,
    salary_inr_senior: tuple,
    salary_usd_entry: tuple,
    salary_usd_mid: tuple,
    salary_usd_senior: tuple,
    growth_rate: int,
    demand: str,
    outlook: str,
    automation_risk: str,
    certifications: List[str],
    related_careers: List[str],
    industries: List[str],
    remote_friendly: bool,
    travel: str,
    work_life_balance: str,
    keywords: List[str]
) -> Dict[str, Any]:
    """Helper function to create a career entry with consistent structure."""
    return {
        "id": id,
        "name": name,
        "category": category,
        "subcategory": subcategory,
        "description": description,
        "required_skills": required_skills,
        "preferred_skills": preferred_skills,
        "education": {
            "minimum": education_min,
            "preferred": education_pref,
            "alternatives": education_alt
        },
        "experience_levels": {
            "entry": "0-2 years",
            "mid": "3-5 years",
            "senior": "6+ years"
        },
        "salary_inr": {
            "entry": {"min": salary_inr_entry[0], "max": salary_inr_entry[1]},
            "mid": {"min": salary_inr_mid[0], "max": salary_inr_mid[1]},
            "senior": {"min": salary_inr_senior[0], "max": salary_inr_senior[1]}
        },
        "salary_usd": {
            "entry": {"min": salary_usd_entry[0], "max": salary_usd_entry[1]},
            "mid": {"min": salary_usd_mid[0], "max": salary_usd_mid[1]},
            "senior": {"min": salary_usd_senior[0], "max": salary_usd_senior[1]}
        },
        "growth": {
            "rate": growth_rate,
            "demand": demand,
            "outlook": outlook,
            "automation_risk": automation_risk
        },
        "certifications": certifications,
        "related_careers": related_careers,
        "industries": industries,
        "work_environment": {
            "remote_friendly": remote_friendly,
            "travel": travel,
            "work_life_balance": work_life_balance
        },
        "keywords": keywords
    }


